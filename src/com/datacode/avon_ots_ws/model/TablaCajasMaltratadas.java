package com.datacode.avon_ots_ws.model;

public class TablaCajasMaltratadas {

	private String registro;
	private int noCaja;
	private String despegada;
	private String averiada;
	private String mojada;
	
	public String getRegistro() {
		return registro;
	}
	public void setRegistro(String registro) {
		this.registro = registro;
	}
	public int getNoCaja() {
		return noCaja;
	}
	public void setNoCaja(int noCaja) {
		this.noCaja = noCaja;
	}
	public String getDespegada() {
		return despegada;
	}
	public void setDespegada(String despegada) {
		this.despegada = despegada;
	}
	public String getAveriada() {
		return averiada;
	}
	public void setAveriada(String averiada) {
		this.averiada = averiada;
	}
	public String getMojada() {
		return mojada;
	}
	public void setMojada(String mojada) {
		this.mojada = mojada;
	}
}
