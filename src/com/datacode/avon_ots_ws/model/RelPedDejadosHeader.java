package com.datacode.avon_ots_ws.model;

public class RelPedDejadosHeader {

	private String zona;
	private String ruta;
	private String campania;
	private String nombreAyudante;
	private String nombreChofer;
	private String poblacion;
	private String nombreEntrega;
	private String nombreRecibe;
	public String getZona() {
		return zona;
	}
	public void setZona(String zona) {
		this.zona = zona;
	}
	public String getRuta() {
		return ruta;
	}
	public void setRuta(String ruta) {
		this.ruta = ruta;
	}
	public String getCampania() {
		return campania;
	}
	public void setCampania(String campania) {
		this.campania = campania;
	}
	public String getNombreAyudante() {
		return nombreAyudante;
	}
	public void setNombreAyudante(String nombreAyudante) {
		this.nombreAyudante = nombreAyudante;
	}
	public String getNombreChofer() {
		return nombreChofer;
	}
	public void setNombreChofer(String nombreChofer) {
		this.nombreChofer = nombreChofer;
	}
	public String getPoblacion() {
		return poblacion;
	}
	public void setPoblacion(String poblacion) {
		this.poblacion = poblacion;
	}
	public String getNombreEntrega() {
		return nombreEntrega;
	}
	public void setNombreEntrega(String nombreEntrega) {
		this.nombreEntrega = nombreEntrega;
	}
	public String getNombreRecibe() {
		return nombreRecibe;
	}
	public void setNombreRecibe(String nombreRecibe) {
		this.nombreRecibe = nombreRecibe;
	}
}
