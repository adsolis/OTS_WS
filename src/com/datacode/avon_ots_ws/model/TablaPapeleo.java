package com.datacode.avon_ots_ws.model;

public class TablaPapeleo {

	private String panoram;
	private String portaf;
	private String papGerenZonal;
	private String papPortead;
	private String otros;
	private int totCajas;
	
	public String getPanoram() {
		return panoram;
	}
	public void setPanoram(String panoram) {
		this.panoram = panoram;
	}
	public String getPortaf() {
		return portaf;
	}
	public void setPortaf(String portaf) {
		this.portaf = portaf;
	}
	public String getPapGerenZonal() {
		return papGerenZonal;
	}
	public void setPapGerenZonal(String papGerenZonal) {
		this.papGerenZonal = papGerenZonal;
	}
	public String getPapPortead() {
		return papPortead;
	}
	public void setPapPortead(String papPortead) {
		this.papPortead = papPortead;
	}
	public String getOtros() {
		return otros;
	}
	public void setOtros(String otros) {
		this.otros = otros;
	}
	public int getTotCajas() {
		return totCajas;
	}
	public void setTotCajas(int totCajas) {
		this.totCajas = totCajas;
	}
	
}
