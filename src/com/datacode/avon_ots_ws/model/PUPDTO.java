package com.datacode.avon_ots_ws.model;

public class PUPDTO {
	
	Integer idPUP;
	String correo;
	String nombre;

	public Integer getIdPUP() {
		return idPUP;
	}
	public void setIdPUP(Integer idPUP) {
		this.idPUP = idPUP;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
